#include <iostream>

using namespace std;

int main() {
  int *idade1 = new int;
  int *idade2 = new int;
  cout << "Qual é a idade da primeira pessoa?\n";
  cin >> *idade1;
  cout << "Qual é a idade da segunda pessoa?\n";
  cin >> *idade2;

  // Ponteiro que vai apontar para a váriavel
  float *media = new float;
  // O * é o local de valor de memória, ou seja, na media
  *media = (*idade1 + *idade2) / 2;
  cout << "A média das idades é: " << *media << endl;
}
